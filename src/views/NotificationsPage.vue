<template>
  <LayoutPage>
    <template #default>
      <p v-if="loading">Cargando...</p>
      <div v-else-if="evento">
        <div class="text-center">
          <h1 class="text-4xl font-extrabold text-gray-900">Notificaciones</h1>
        </div>
        
        <!-- üìå Lista de notificaciones -->
        <div class="border rounded-lg shadow p-4 mt-2 flex-grow h-[50vh] overflow-y-auto bg-white">
          <ul v-if="notifications.length > 0">
            <li v-for="notification in sortedNotifications" :key="notification.id" class="p-3 border-b">
              <p class="text-lg font-medium">
                {{ notification.message }}
              </p>
            </li>
          </ul>
          <p v-else class="text-gray-500">No hay notificaciones registradas para este evento.</p>
        </div>
      </div>
    </template>
  </LayoutPage>
</template>

<script setup>
import { onMounted, ref, watchEffect,computed,watch } from "vue";
import { storeToRefs } from "pinia";
import { useEventStore } from "@/stores/event";
import { useRoute } from "vue-router";
import LayoutPage from "@/Layout.vue";
import { useAuthStore } from "@/stores/auth";

// Estado y store
const eventStore = useEventStore();
const { evento, loading, error, notifications } = storeToRefs(eventStore);
const route = useRoute();
const authStore = useAuthStore();

const sortedNotifications = computed(() => {
  return notifications.value.sort((a, b) => new Date(b.created_at) - new Date(a.created_at)); // Cambia 'created_at' por el campo de tu notificaci√≥n
});

// Cargar evento y notificaciones al montar
onMounted(async () => {
  await eventStore.fetch(route.params.slug); // Obtener evento
});

// Solo llamar a fetchNotifications cuando evento.id cambie y sea v√°lido
watch(
  () => evento.value?.id, // Solo observar cambios en evento.id
  async (newEventId) => {
    if (newEventId) {
      const userId = authStore.user.id;
      await eventStore.fetchNotifications(newEventId, userId);
    }
  },
  { immediate: true } // Llamar al iniciar si evento.id ya est√° disponible
);

// Esperar que el evento est√© listo y luego cargar notificaciones

</script>
